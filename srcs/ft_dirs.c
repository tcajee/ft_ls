#include "../libft/incs/libft.h"

int	ft_dirs(char **argv, t_flags *flags)
{
	int i;

	i = 0;
	while (argv[i] && *flags)
		FT_(!ft_check_dirs(argv[i++]), 0);
	return (1);
}

